    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (txtTenKH.getText().isEmpty() || txtCMND.getText().isEmpty() || txtDiaChi.getText().isEmpty() || txtMaKH.getText().isEmpty() || txtNgayDK.getText().isEmpty() || txtNgaySinh.getText().isEmpty() || txtSDT.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Phải điền tất cả thông tin!");
        } else {
            try{
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                sdf.setLenient(false);
                String maKH = txtMaKH.getText();
                String ten = txtTenKH.getText();
                String cmnd = txtCMND.getText();
                String diaChi = txtDiaChi.getText();
                String gioiTinh = rdNam.isSelected() ? "Nam" : "Nữ";
                Date ngayDK = null;
                Date ngaySinh = null;
                try {
                    ngaySinh = sdf.parse(txtNgaySinh.getText());
                    ngayDK = sdf.parse(txtNgayDK.getText());
                } catch (ParseException ex) {
                    JOptionPane.showMessageDialog(this, "Nhập sai định dạng ngày!");
                }
                String sdt = txtSDT.getText();;
                String loaiDien = cboLoaiDien.getSelectedItem().toString();
                HoTieuThuDien httd=new HoTieuThuDien(maKH, ten, gioiTinh, ngaySinh, cmnd, sdt, ngayDK, diaChi, loaiDien);
                HoTieuThuController httc=new HoTieuThuController();
                httc.insert(httd);
                getDataFromDB();
            }catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
            ex.printStackTrace();
        }
        }
    }//GEN-LAST:event_btnThemActionPerformed


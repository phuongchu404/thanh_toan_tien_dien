    public HoTieuThuDien getDataFromResultSet(ResultSet rs) {
        HoTieuThuDien httd = null;
        try {
            String maKH = rs.getString("maKH");
            String hoTen = rs.getString("hoTen");
            String CMND = rs.getString("CMND");
            String diaChi = rs.getString("diaChi");
            String gioiTinh = rs.getString("gioiTinh");
            Date ngaySinh = new Date(rs.getDate("ngaySinh").getTime());
            String sdt = rs.getString("sdt");
            Date ngayDangKi = new Date(rs.getDate("ngayDangKi").getTime());
            String loaiDien = rs.getString("loaiDien");
            httd = new HoTieuThuDien(maKH, hoTen, gioiTinh, ngaySinh, CMND, sdt, ngayDangKi, diaChi, loaiDien);
        } catch (SQLException ex) {
            Logger.getLogger(HoTieuThuController.class.getName()).log(Level.SEVERE, null, ex);
        }
        return httd;
    }

    public void getDataFromDB() {
        try {
            dtm.setRowCount(0);
            String sqlQuery = "Select * from HoTieuThu";
            dbConnect=new DBConnect();
            connection=dbConnect.connect();
            PreparedStatement ps = connection.prepareStatement(sqlQuery);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                HoTieuThuDien httd = getDataFromResultSet(rs);
                dtm.addRow(httd.toStringArray());
            }
            ps.close();
        } catch (SQLException ex) {
            Logger.getLogger(HoTieuThuController.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
